# Definiciones

''' Algoritmo: es una secuencia de pasos bien definida que deben realizarse en forma
secuencial para lograr un resultado. No debe dejar lugar a dudas sobre el significado de
cada paso (no ambigua). 


Lenguaje de programación: es un lenguaje formal que puede ser utilizado por un
programador para dar instrucciones a una computadora con el fin de controlar el
comportamiento físico/lógico para obtener algún resultado. 

● Compilado: se escriben las instrucciones (programa) y luego un programa
especial llamado compilador las traduce todas juntas a otro lenguaje llamado
de maquina que entiende el microprocesador. Ejemplo: Python

● Interpretado: las instrucciones se leen y ejecutan una por una a medida que se
ingresan. Podemos escribir todo el programa y luego dárselo al intérprete, esto
se llama escribir un guion. 

Sentencia: cada instrucción que puede ejecutarse dentro de un programa. 

Expresión: un conjunto de valores y operandos que siempre resuelve a un resultado'''

